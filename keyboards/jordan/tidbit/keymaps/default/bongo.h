void eval_anim_state(void)
{
    switch (anim_state)
    {
    case Idle:
        if (shouldTap) // Idle to Tap
        {
            anim_state = Tap;
        }
        break;

    case Prep:
        if (shouldTap) // Prep to Tap
        {
            anim_state = Tap;
        }
        else if (timer_elapsed32(idle_timeout_timer) >= IDLE_TIMEOUT) // Prep to Idle
        {
            anim_state = Idle;
            current_idle_frame = 0;
        }
        break;

    case Tap:
        if (!shouldTap) // Tap to Prep
            idle_timeout_timer = timer_read32();
        break;

    default:
        break;
    }
    shouldTap = false;
}

static void bongo_prep(void)
{
    anim_state = Prep;
    anim_timer = timer_read32();
}

static void bongo_tap(enum tap_sides direction)
{
    shouldTap = true;
    anim_state = Prep;
    tapDirection = direction;
    anim_timer = timer_read32();
}

static void draw_bongo(bool minimal)
{
    eval_anim_state();
    oled_set_cursor(0, 0);

    switch (anim_state)
    {
    case Idle:
        oled_write_raw_P(idle_minimal[abs((IDLE_FRAMES - 1) - current_idle_frame)], ANIM_SIZE);

        if (timer_elapsed32(anim_timer) > ANIM_FRAME_DURATION)
        {
            current_idle_frame = (current_idle_frame + 1) % IDLE_FRAMES;
            anim_timer = timer_read32();
        }
        break;

    case Prep:
        oled_write_raw_P(prep_minimal[0], ANIM_SIZE);
        break;

    case Tap:
    {
        uint32_t frame = tapDirection == Right ? 1 : 0;

        oled_write_raw_P(tap_minimal[frame], ANIM_SIZE);

        if (timer_elapsed32(anim_timer) > TAP_FRAME_DURATION)
        {
            current_tap_frame++;
            if (current_tap_frame >= TAP_FRAMES)
            {
                current_tap_frame = 0;
                bongo_prep();
            }

            anim_timer = timer_read32();
        }
        break;
    }

    default:
        break;
    }
}
